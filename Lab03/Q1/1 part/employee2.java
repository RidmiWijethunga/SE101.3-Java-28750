
package com.mycompany.mainemp2;

public class employee2 {
    
    private String name;
    private int age;
    private double salary;
    
    
    // Constructor
    public employee2(String name, int age, double salary) {
        this.name = name;
        this.age = age;
        this.salary = salary;
    }
    // Getter for Name
    public String getName() {
        return name;
    }

    // Getter for Age
    public int getAge() {
        return age;
    }

    // Getter for Salary
    public double getSalary() {
        return salary;
    }
    
}
